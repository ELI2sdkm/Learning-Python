x=eval(input('你每天大概睡几个小时（请输入数字）？'))
if 8<=x<=10:
    print('完美的睡眠！继续保持！')
elif 10<x<=12:
    print('还行还行，确定上课不会迟到？')
elif 12<x<=24:
    print('您别起了，超度吧超度吧！')
elif 6<=x<8:
    print('睡眠时间有点少呀，如果影响到正常生活的话快去看看医生吧！')
elif 0<x<6:
    print('睡得饱吗......')
elif x==0:
    print('出现了！永动机！！！')
else:
    print('恁有时间宝石是吧？')